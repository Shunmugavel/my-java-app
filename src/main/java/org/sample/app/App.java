package org.sample.app;

public class App {

    public int add(int v1, int v2){
        return v1 + v2;
    }

    public int subtract(int v1, int v2){
        return v1 - v2;
    }

    public int multiply(int v1, int v2){
        return v1 * v2;
    }

    public int divide(int v1, int v2){
        return v1/v2;
    }
}
